# --- 3. DATABASE SETUP ---
@st.cache_resource(ttl=600)
def init_db():
    # Connect using environment variable or Streamlit secrets
    db_url = os.environ.get("DATABASE_URL")
    if not db_url:
        try:
            db_url = st.secrets["DATABASE_URL"]
        except Exception:
            st.error("DATABASE_URL not found. Please set it in .env or Streamlit Secrets.")
            st.stop()
            
    try:
        conn = psycopg2.connect(db_url)
        c = conn.cursor(cursor_factory=RealDictCursor)
        
        c.execute('''
            CREATE TABLE IF NOT EXISTS students (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                name TEXT NOT NULL,
                student_class TEXT,
                monthly_fees NUMERIC,
                parent_name TEXT,
                contact TEXT,
                is_paid BOOLEAN DEFAULT FALSE,
                is_archived BOOLEAN DEFAULT FALSE,
                last_paid_date TEXT
            )
        ''')
        c.execute('''
            CREATE TABLE IF NOT EXISTS expenses (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                item TEXT NOT NULL,
                amount NUMERIC,
                date TEXT
            )
        ''')
        c.execute('''
            CREATE TABLE IF NOT EXISTS tasks (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                task TEXT NOT NULL,
                is_done BOOLEAN DEFAULT FALSE
            )
        ''')
        c.execute('''
            CREATE TABLE IF NOT EXISTS attendance (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                student_id BIGINT REFERENCES students(id) ON DELETE CASCADE,
                date TEXT,
                status TEXT
            )
        ''')
        c.execute('''
            CREATE TABLE IF NOT EXISTS exams (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                student_id BIGINT REFERENCES students(id) ON DELETE CASCADE,
                exam_name TEXT,
                score NUMERIC,
                total_marks NUMERIC,
                date TEXT
            )
        ''')
        c.execute('''
            CREATE TABLE IF NOT EXISTS admin_auth (
                id INT PRIMARY KEY,
                password_hash TEXT
            )
        ''')
        
        # Seed the default admin password if the auth table is empty
        c.execute("SELECT password_hash FROM admin_auth WHERE id=1")
        if not c.fetchone():
            default_hash = "240be518fabd2724ddb6f04eeb1da5967448d7e831c08c8fa822809f74c720a9"
            c.execute("INSERT INTO admin_auth (id, password_hash) VALUES (1, %s)", (default_hash,))
            
        conn.commit()
        return conn
    except psycopg2.OperationalError as e:
        st.error("🚨 **Database Connection Rejected by Supabase**")
        st.warning("If your database password has special characters (like `@`, `#`, `?`), it breaks the connection URL! You must URL-encode them. (For example, change `@` to `%40` in your password inside the URL).")
        st.info(f"Technical Details: {e}")
        st.stop()

def get_db_connection():
    # Now securely cached with a 10-minute Time-To-Live to prevent crashing the connection pool
    return init_db()

conn = get_db_connection()

