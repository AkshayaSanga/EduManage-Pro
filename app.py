# --- 5. PREMIUM PDF GENERATOR (Fixed Bytearray Error) ---
def generate_pdf(name, parent_name, class_name, amount, date_str):
    pdf = FPDF()
    pdf.add_page()
    
    # Dark Carbon Header
    pdf.set_fill_color(29, 29, 29)
    pdf.rect(0, 0, 210, 40, 'F')
    
    pdf.set_font("Arial", 'B', 24)
    pdf.set_text_color(251, 191, 36) # Amber/Gold
    pdf.cell(0, 20, "EDU-MANAGE RECEIPT", ln=True, align='C')
    pdf.set_font("Arial", 'I', 10)
    pdf.cell(0, 10, f"Date: {date_str}", ln=True, align='C')
    
    pdf.ln(20)
    
    # Table Content
    pdf.set_text_color(0, 0, 0)
    pdf.set_font("Arial", 'B', 12)
    pdf.set_fill_color(240, 240, 240)
    pdf.cell(95, 12, "Description", border=1, fill=True)
    pdf.cell(95, 12, "Details", border=1, fill=True, ln=True)
    
    pdf.set_font("Arial", '', 11)
    rows = [
        ("Student Name", name),
        ("Parent Name", parent_name),
        ("Class", class_name),
        ("Amount Paid", f"INR {amount}")
    ]
    
    for label, val in rows:
        pdf.cell(95, 10, label, border=1)
        pdf.cell(95, 10, str(val), border=1, ln=True)
    
    pdf.ln(20)
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 10, "Status: PAID", ln=True, align='R')
    pdf.set_draw_color(29, 29, 29)
    pdf.line(140, pdf.get_y(), 200, pdf.get_y())
    pdf.set_font("Arial", 'I', 8)
    pdf.cell(0, 10, "Authorized Signature", align='R')
    
    return bytes(pdf.output(dest='S'))

# --- 5.1 PREMIUM REPORT CARD PDF GENERATOR ---
def generate_report_card_pdf(name, class_name, att_percent, present_days, total_days, exam_records, date_str):
    pdf = FPDF()
    pdf.add_page()
    
    # Dark Carbon Header
    pdf.set_fill_color(29, 29, 29)
    pdf.rect(0, 0, 210, 40, 'F')
    
    pdf.set_font("Arial", 'B', 24)
    pdf.set_text_color(251, 191, 36) # Amber/Gold
    pdf.cell(0, 20, "ACADEMIC REPORT CARD", ln=True, align='C')
    pdf.set_font("Arial", 'I', 10)
    pdf.cell(0, 10, f"Date Generated: {date_str}", ln=True, align='C')
    
    pdf.ln(20)
    
    # Student Info & Attendance Summary
    pdf.set_text_color(0, 0, 0)
    pdf.set_fill_color(240, 240, 240)
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 10, "1. Student & Attendance Profile", ln=True, fill=True)
    pdf.set_font("Arial", '', 11)
    pdf.cell(100, 10, f"Name: {name}", ln=False)
    pdf.cell(90, 10, f"Class: {class_name}", ln=True, align='R')
    if total_days > 0:
        pdf.cell(100, 10, f"Days Present: {present_days} out of {total_days}", ln=False)
        pdf.cell(90, 10, f"Attendance: {att_percent:.1f}%", ln=True, align='R')
    else:
        pdf.cell(0, 10, "No attendance records available.", ln=True)
        
    pdf.ln(10)
    
    # Exam Scores
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 10, "2. Academic Performance", ln=True, fill=True)
    
    if exam_records:
        pdf.set_font("Arial", 'B', 11)
        pdf.cell(70, 10, "Exam / Test Name", border=1)
        pdf.cell(40, 10, "Date", border=1)
        pdf.cell(40, 10, "Score Obtained", border=1)
        pdf.cell(40, 10, "Percentage", border=1, ln=True)
        
        pdf.set_font("Arial", '', 11)
        for ex in exam_records:
            pct = (ex['score'] / ex['total_marks']) * 100 if ex['total_marks'] > 0 else 0
            pdf.cell(70, 10, str(ex['exam_name']), border=1)
            pdf.cell(40, 10, str(ex['date']), border=1)
            pdf.cell(40, 10, f"{ex['score']} / {ex['total_marks']}", border=1)
            pdf.cell(40, 10, f"{pct:.1f}%", border=1, ln=True)
    else:
        pdf.set_font("Arial", '', 11)
        pdf.cell(0, 10, "No exam records found.", ln=True)
        
    return bytes(pdf.output(dest='S'))

